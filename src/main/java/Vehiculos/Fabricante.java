package Vehiculos;

public class Fabricante {
	private String nombre;
	private Pais pais;
	
	public String getNombre() {
		return this.nombre;
	}
	public void setNombre(String n) {
		this.nombre=n;
	}
	public Pais getPais() {
		return this.pais;
	}
	public void setaPais(Pais p) {
		this.pais=p;
	}
}
